    private void txtCedulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCedulaActionPerformed
        String cedula = txtCedula.getText().trim();
        // Obtener el texto del campo de texto y eliminar espacios en blanco

        try {
            // Validar que la cÃ©dula tenga 10 dÃ­gitos
            if (cedula.length() != 10) {
                JOptionPane.showMessageDialog(this, "La cÃ©dula debe tener 10 dÃ­gitos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validar la cÃ©dula usando el algoritmo proporcionado
            if (validarCedula(cedula)) {
                JOptionPane.showMessageDialog(this, "CÃ©dula vÃ¡lida.", "Ãxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "CÃ©dula invÃ¡lida.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La cÃ©dula debe contener solo nÃºmeros.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "OcurriÃ³ un error inesperado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }

        private boolean validarCedula(String cedula) {
            int suma = 0;

            // Verificar que la cÃ©dula tenga 10 dÃ­gitos
            if (cedula.length() != 10) {
                return false;
            }

            int a[] = new int[cedula.length() / 2];
            int b[] = new int[(cedula.length() / 2)];
            int c = 0;
            int d = 1;

            for (int i = 0; i < cedula.length() / 2; i++) {
                a[i] = Integer.parseInt(String.valueOf(cedula.charAt(c)));
                c = c + 2;
                if (i < (cedula.length() / 2) - 1) {
                    b[i] = Integer.parseInt(String.valueOf(cedula.charAt(d)));
                    d = d + 2;
                }
            }

            for (int i = 0; i < a.length; i++) {
                a[i] = a[i] * 2;
                if (a[i] > 9) {
                    a[i] = a[i] - 9;
                }
                suma = suma + a[i] + b[i];
            }

            int aux = suma / 10;
            int dec = (aux + 1) * 10;
            if ((dec - suma) == Integer.parseInt(String.valueOf(cedula.charAt(cedula.length() - 1)))) {
                return true;
            } else if (suma % 10 == 0 && cedula.charAt(cedula.length() - 1) == '0') {
                return true;
            } else {
                return false;
            }
    }//GEN-LAST:event_txtCedulaActionPerformed

