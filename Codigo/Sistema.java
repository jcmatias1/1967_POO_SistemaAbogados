    public void ListarCliente() {
        modelo = (DefaultTableModel) TableCliente.getModel();
        modelo.setRowCount(0); // Limpiar la tabla antes de agregar nuevos datos

        // Supongamos que tienes una lista de clientes llamada "listaClientes"
        ArrayList<Cliente> listaClientes = obtenerListaClientes(); // MÃ©todo que retorna la lista de clientes

        for (Cliente cliente : listaClientes) {
            Object[] ob = new Object[5];
            ob[0] = cliente.getNombre();
            ob[1] = cliente.getApellido();
            ob[2] = cliente.getTelefono();
            ob[3] = cliente.getEmail();
            ob[4] = cliente.getCedula();

            modelo.addRow(ob); // Agregar la fila a la tabla
        }

        TableCliente.setModel(modelo); // Establecer el modelo de la tabla
    
    }
    
    
